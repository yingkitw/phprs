<?php
// Operators Example
// Demonstrates PHP operators

$a = 10;
$b = 3;

// Arithmetic operations
echo "a + b = " . ($a + $b) . "\n";
echo "a - b = " . ($a - $b) . "\n";
echo "a * b = " . ($a * $b) . "\n";
echo "a / b = " . ($a / $b) . "\n";
echo "a % b = " . ($a % $b) . "\n";

// String interpolation
$name = "PHP-RS";
echo "Hello $name!\n";

// String concatenation
echo "Result: " . ($a * $b + 2) . "\n";

// Built-in functions
echo "strlen: " . strlen("hello") . "\n";
echo strtoupper("operators work") . "\n";
