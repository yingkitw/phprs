<?php
// String Operations Example
// Demonstrates string concatenation and output

$greeting = 'Hello';
$name = 'PHP-RS';
$punctuation = '!';

// String concatenation
$message = $greeting . ', ' . $name . $punctuation;
echo $message;
echo "\n";

// Multiple echo statements
echo 'PHP';
echo ' ';
echo 'is';
echo ' ';
echo 'awesome!';
echo "\n";

// Combining strings and numbers
$version = 0.1;
echo 'Version: ';
echo $version;
echo "\n";

$year = 2024;
echo 'Year: ';
echo $year;
echo "\n";
