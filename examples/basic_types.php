<?php
// Basic PHP Types Example
// Demonstrates creating and manipulating basic PHP types

// Integer
$int = 42;
var_dump($int);

// Float
$float = 3.14159;
var_dump($float);

// String
$str = "Hello, PHP-RS!";
var_dump($str);

// Boolean
$true = true;
$false = false;
var_dump($true);
var_dump($false);

// Null
$null = null;
var_dump($null);

// Array
$arr = [1, 2, 3];
var_dump($arr);

