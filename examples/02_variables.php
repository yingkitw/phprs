<?php
// Variables Example
// Demonstrates variable assignment and output

// Integer variable
$age = 25;
echo 'Age: ';
echo $age;
echo "\n";

// String variable
$name = 'PHP-RS';
echo 'Name: ';
echo $name;
echo "\n";

// Using variables in expressions
$x = 10;
$y = 20;
$sum = $x + $y;
echo 'Sum: ';
echo $sum;
echo "\n";

// Multiple operations
$a = 5;
$b = 3;
echo 'Addition: ';
echo $a + $b;
echo "\n";
echo 'Subtraction: ';
echo $a - $b;
echo "\n";
echo 'Multiplication: ';
echo $a * $b;
echo "\n";
echo 'Division: ';
echo $a / $b;
echo "\n";
