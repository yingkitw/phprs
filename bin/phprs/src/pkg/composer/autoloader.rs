//! PSR-4 Autoloader Generator

use super::super::error::Result;
use std::collections::HashMap;
use std::fs;
use std::path::Path;

/// Generate vendor/autoload.php
pub fn generate_autoloader(
    vendor_dir: &Path,
    autoload_mappings: &HashMap<String, String>,
) -> Result<()> {
    let autoload_php = vendor_dir.join("autoload.php");

    let content = generate_autoload_content(autoload_mappings);

    fs::write(&autoload_php, content)?;
    log::info!("Generated vendor/autoload.php");

    Ok(())
}

/// Generate autoload.php content
fn generate_autoload_content(autoload_mappings: &HashMap<String, String>) -> String {
    let psr4_map = format_map(autoload_mappings);

    format!(
        r#"<?php
// Generated by phprs pkg (PHP-RS Package Manager)

/**
 * PSR-4 Autoloader
 *
 * @see https://www.php-fig.org/psr/psr-4/
 */
class ComposerAutoloaderInit{{
    private static $loader;

    public static function getLoader(){{
        if (null !== self::$loader) {{
            return self::$loader;
        }}

        spl_autoload_register(function($class) {{
            // PSR-4 autoloading
            $prefixes = {psr4_map};

            foreach ($prefixes as $prefix => $base_dir) {{
                // Check if class starts with namespace prefix
                $len = strlen($prefix);
                if (strncmp($prefix, $class, $len) === 0) {{
                    // Get relative class name
                    $relative_class = substr($class, $len);

                    // Replace namespace separators with directory separators
                    $file = $base_dir . str_replace('\\', '/', $relative_class) . '.php';

                    // If file exists, require it
                    if (file_exists($file)) {{
                        require $file;
                        return;
                    }}
                }}
            }}
        }});

        return self::$loader;
    }}
}}

return ComposerAutoloaderInit::getLoader();
"#,
        psr4_map = psr4_map
    )
}

/// Format PSR-4 map for PHP
fn format_map(map: &HashMap<String, String>) -> String {
    let entries: Vec<String> = map
        .iter()
        .map(|(k, v)| format!("'{}' => '{}'", k, v))
        .collect();

    format!("array({})", entries.join(", "))
}

/// Generate classmap autoloader
pub fn generate_classmap(
    vendor_dir: &Path,
    classmap: &HashMap<String, String>,
) -> Result<()> {
    if classmap.is_empty() {
        return Ok(());
    }

    let classmap_php = vendor_dir.join("autoload_classmap.php");

    let entries: Vec<String> = classmap
        .iter()
        .map(|(class, file)| format!("'{}' => '{}',", class, file))
        .collect();

    let content = format!(
        r#"<?php
// Generated by phprs pkg (PHP-RS Package Manager)
// Classmap autoloader

return array(
{}
);
"#,
        entries.join("\n    ")
    );

    fs::write(&classmap_php, content)?;
    log::info!("Generated vendor/autoload_classmap.php");

    Ok(())
}

