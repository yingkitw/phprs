#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_format_map() {
        let mut map = HashMap::new();
        map.insert("App\\".to_string(), "src/".to_string());

        let result = format_map(&map);
        assert!(result.contains("App\\"));
        assert!(result.contains("src/"));
    }

    #[test]
    fn test_generate_autoload_content() {
        let mut map = HashMap::new();
        map.insert("App\\".to_string(), "src/".to_string());

        let content = generate_autoload_content(&map);

        assert!(content.contains("<?php"));
        assert!(content.contains("ComposerAutoloaderInit"));
        assert!(content.contains("spl_autoload_register"));
        assert!(content.contains("App\\"));
    }
}
